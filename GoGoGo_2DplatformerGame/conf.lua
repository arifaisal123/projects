function love.conf(t)
    -- Gives the title of the project
    t.title = "Go! Go! Go!"
    
    -- Gives the Love version to be used
    t.version = "11.4"
    
    t.console = true
    
    -- Sets the project window size
    t.window.width = 1280
    t.window.height = 720
    t.window.vsync = 0
end